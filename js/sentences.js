class SentenceService {
  constructor() {
    this.sentences = SENTENCES;
    this.usedIds = new Set();
  }

  getSentence(targetWordCount) {
    // filter to match exactly, or within +/- 2 if none available
    let candidates = this.sentences.filter(s => s.wordCount === targetWordCount && !this.usedIds.has(s.id));
    
    if (candidates.length === 0) {
      candidates = this.sentences.filter(s => Math.abs(s.wordCount - targetWordCount) <= 2 && !this.usedIds.has(s.id));
    }

    if (candidates.length === 0) {
      // Reset if we run out
      this.usedIds.clear();
      return this.getSentence(targetWordCount);
    }

    const sentence = candidates[Math.floor(Math.random() * candidates.length)];
    this.usedIds.add(sentence.id);
    return sentence;
  }

  getRandomWords(count, excludeWords) {
    // Extract random unique words from the dataset that aren't in excludeWords
    const excludeSet = new Set(excludeWords.map(w => w.toLowerCase()));
    const allWords = [];
    this.sentences.forEach(s => {
      s.text.split(' ').forEach(w => allWords.push(w));
    });
    
    const uniqueWords = [...new Set(allWords)].filter(w => !excludeSet.has(w.toLowerCase()));
    
    const result = [];
    for(let i=0; i<count; i++) {
      if(uniqueWords.length === 0) break;
      const idx = Math.floor(Math.random() * uniqueWords.length);
      result.push(uniqueWords.splice(idx, 1)[0]);
    }
    return result;
  }

  shuffleWords(words) {
    let shuffled = [...words];
    for (let i = shuffled.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    if (shuffled.join(' ') === words.join(' ') && shuffled.length > 1) {
      [shuffled[0], shuffled[1]] = [shuffled[1], shuffled[0]];
    }
    return shuffled;
  }

  reset() {
    this.usedIds.clear();
  }
}
